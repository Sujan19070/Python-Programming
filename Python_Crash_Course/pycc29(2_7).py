#Store a name with whitespace character
name = "\t\n Nabib Ahamed \t\n"

# Print the name with whitespace
print("Original name with whitespace:")
print(name)

# Using lstrip()
print("Using lstrip():")
print(name.lstrip())

# Using rstrip()
print("Using rstrip():")
print(name.rstrip())

# Using strip()
print("Using strip():")
print(name.strip())